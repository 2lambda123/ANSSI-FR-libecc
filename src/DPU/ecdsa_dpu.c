int ecdsa_signature_verification_digest (const u8 *sig, const u8 *pub_key,
              const u8 *hash)
{
    prj_pt uG, vY, W_prime;
    nn nn_array[6];
    nn *e = &nn_array[0];
    nn *tmp = &nn_array[1];
    nn *sinv = &nn_array[2];
    nn *u = &nn_array[3];
    nn *v = &nn_array[4];
    nn *r_prime = &nn_array[5];
    aff_pt W_prime_aff;
    prj_pt_src_t G, Y;
    bitcnt_t rshift, q_bit_len;
    nn_src_t q;
    nn *s, *r;
    nn s_var, r_var;
    u8 hsize;
    int ret;
    ec_params *params_ptr = (ec_params *)&params;
    prj_pt pt;

    u16 coord_len = BYTECEIL(params_ptr->ec_curve.a.ctx->p_bitlen);
    fp_init_from_buf(&(pt.X), params_ptr->ec_curve.a.ctx, pub_key, coord_len);
    fp_init_from_buf(&(pt.Y), params_ptr->ec_curve.a.ctx, pub_key + coord_len, coord_len);
    fp_init_from_buf(&(pt.Z), params_ptr->ec_curve.a.ctx, one, coord_len);

    /* Set the curve */
    pt.crv = (ec_shortw_crv_src_t)&params_ptr->ec_curve;

    /* Mark the point as initialized */
    pt.magic = PRJ_PT_MAGIC;

    G = &(params_ptr->ec_gen);
    Y = &pt;

    /* Zero init points */
    local_memset(&uG, 0, sizeof(prj_pt));
    local_memset(&vY, 0, sizeof(prj_pt));

    q = &(params_ptr->ec_gen_order);
    q_bit_len = params_ptr->ec_gen_order_bitlen;
    u8 q_len = (u8)BYTECEIL(q_bit_len);

    hsize = SHA256_DIGEST_SIZE;

    r = &r_var;
    s = &s_var;
    nn_init_from_buf(r, sig, q_len);
    nn_init_from_buf(s, sig + q_len, q_len);

    /* 1. Reject the signature if r or s is 0. */
    if (nn_iszero(r) || (nn_cmp(r, q) >= 0) ||
        nn_iszero(s) || (nn_cmp(s, q) >= 0)) {
        nn_uninit(r);
        nn_uninit(s);
        ret = -1;
        goto err;
    }

    /* 2. Compute h = H(m) */
    /* Hash is precomputed */

    /*
     * 3. If |h| > bitlen(q), set h to bitlen(q)
     *    leftmost bits of h.
     *
     * Note that it's easier to check here if the truncation
     * needs to be done but implement it using a logical
     * shift at the beginning of step 3. below once the hash
     * has been converted to an integer.
     */
    rshift = 0;
    if ((hsize * 8) > q_bit_len) {
        rshift = (hsize * 8) - q_bit_len;
    }

    /*
     * 4. Compute e = OS2I(h) mod q, by converting h to an integer
     * and reducing it mod q
    */
    nn_init_from_buf(tmp, hash, hsize);
    if (rshift) {
        nn_rshift_fixedlen(tmp, tmp, rshift);
    }
    nn_mod(e, tmp, q);
    nn_modinv(sinv, s, q);

    /* 5. Compute u = (s^-1)e mod q */
    nn_mul(tmp, e, sinv);
    nn_mod(u, tmp, q);

    /* 6. Compute v = (s^-1)r mod q */
    nn_mul_mod(v, r, sinv, q);

    /* 7. Compute W' = uG + vY */
    prj_pt_mul_monty(&uG, u, G);
    prj_pt_mul_monty(&vY, v, Y);
    prj_pt_add_monty(&W_prime, &uG, &vY);
    prj_pt_uninit(&uG);
    prj_pt_uninit(&vY);

    /* 8. If W' is the point at infinity, reject the signature. */
    if (prj_pt_iszero(&W_prime)) {
        ret = -1;
        goto err;
    }

    /* 9. Compute r' = W'_x mod q */
    prj_pt_to_aff(&W_prime_aff, &W_prime);
    nn_mod(r_prime, &(W_prime_aff.x.fp_val), q);
    prj_pt_uninit(&W_prime);
    aff_pt_uninit(&W_prime_aff);

    /* 10. Accept the signature if and only if r equals r' */
    ret = (nn_cmp(r_prime, r) != 0) ? -1 : 0;

err:
    local_memset(nn_array, 0, sizeof(nn_array));
    return ret;
}

