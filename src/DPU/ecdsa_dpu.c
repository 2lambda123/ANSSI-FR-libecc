static void print_nn(char *s, uint8_t *nn_ptr){
    printf("\n%s.val: ", s);
    for (uint32_t cnt =0; cnt < BIT_LEN_WORDS(NN_MAX_BIT_LEN)*4; cnt++) {
        printf ("0x%02X ",*nn_ptr++);
    }
    printf("\n");
    printf("%s.magic 0x%x\n", s, *(uint32_t*)nn_ptr);
    nn_ptr +=4;
    printf("%s.wlen 0x%x\n", s, *nn_ptr);
    printf("\n");
}

